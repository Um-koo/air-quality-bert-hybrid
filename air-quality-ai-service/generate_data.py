import pandas as pd
import os

# 데이터 저장 폴더 확인 (없으면 생성)
os.makedirs('data', exist_ok=True)

# BERT 학습용 샘플 데이터 (텍스트, 라벨)
# Label 0: 좋음/보통, 1: 나쁨/주의, 2: 매우나쁨/위험
data = [
    ("오늘 하늘이 정말 파랗고 공기도 상쾌해요", 0),
    ("날씨가 좋아서 산책하기 딱 좋은 날이네요", 0),
    ("미세먼지 걱정 없이 환기시켜도 되겠어요", 0),
    ("공기가 맑아서 기분까지 좋아지는 아침입니다", 0),
    
    ("하늘이 조금 뿌옇고 먼지가 끼어 있는 것 같아요", 1),
    ("목이 약간 칼칼한 게 미세먼지가 좀 있나 봐요", 1),
    ("오늘은 가급적 마스크를 쓰고 외출하는 게 좋겠어요", 1),
    ("대기 질이 별로 안 좋으니 야외 활동을 자제하세요", 1),
    
    ("황사가 너무 심해서 앞 건물이 안 보일 정도예요", 2),
    ("숨쉬기가 답답하고 눈이 너무 따가워요. 최악이네요", 2),
    ("공사장 먼지랑 매연 때문에 숨을 쉴 수가 없습니다", 2),
    ("정부에서 외출 금지 재난 문자가 왔어요. 공기가 너무 위험해요", 2)
]

# 데이터 뻥튀기 (10번 반복해서 120개 문장 만들기)
df = pd.DataFrame(data * 10, columns=['text', 'label'])

# CSV 저장
file_path = "data/air_quality_texts.csv"
df.to_csv(file_path, index=False, encoding='utf-8-sig')

print(f"✅ 데이터 생성 완료: {file_path}")
print(f"📊 총 데이터 개수: {len(df)}개")